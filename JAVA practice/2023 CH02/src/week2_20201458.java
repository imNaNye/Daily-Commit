public class week2_20201458{
    public static void main(String[] args) {
        final double PI;
        PI = 3.14;
        int radius;
        radius = 5;
        double area = PI * radius * radius; //자동 타입 변환
        System.out.println("원의 반지름은 " + radius + "cm입니다.");
        System.out.println("원의 면적은 " + area + "cm2 입니다.");
    }
}
